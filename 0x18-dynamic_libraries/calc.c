#include "calc.h"

/**
 * add - Addition of ints
 * a: Integer for adding
 * b: Integer for adding
 *
 * Return: Addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtraction of ints
 * a: integer
 * b: integer
 *
 * Return: Subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplication of ints
 * a: integer
 * b: integer
 *
 * Return: Product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Division of ints
 * a: integer
 * b: integer
 *
 * Return: Division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Modular of ints
 * a: integer
 * b: integer
 *
 * Return: Remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
